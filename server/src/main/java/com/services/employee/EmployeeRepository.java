package com.services.employee;

import io.micronaut.core.annotation.NonNull;
import io.micronaut.data.annotation.Query;
import io.micronaut.data.jdbc.annotation.JdbcRepository;
import io.micronaut.data.model.query.builder.sql.Dialect;
import io.micronaut.data.repository.CrudRepository;

import javax.validation.Valid;
import javax.validation.constraints.NotNull;
import java.util.Optional;
import java.util.UUID;
@JdbcRepository(dialect = Dialect.POSTGRES)
public interface EmployeeRepository extends CrudRepository<Employee, UUID> {
    @Query(value = "UPDATE employees SET active = false WHERE id = :id")
    Optional<Employee> softDeleteById(@NotNull String id);

    @Override
    <S extends Employee> S save(@Valid @NotNull @NonNull S entity);

    @Override
    <S extends Employee> S update(@Valid @NotNull @NonNull S entity);

    Optional<Employee> findById(@NonNull UUID id);

































































































































































































































































































































































}
